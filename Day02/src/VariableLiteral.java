public class VariableLiteral {
	public static void main(String[] args) {
		//1.나는 홍길동입니다.
		System.out.println("나는 \"홍길동\" 입니다.");
		System.out.println("나는 '홍길동' 입니다.");
		
		//2.*3개를 찍는데 7칸씩 띄워서 출력
		//\t(tab)
		System.out.println("*\t*\t*");
		System.out.println("*	*	*");
		
		//2.*3개를 찍는데 한칸씩 아래로
		System.out.println("*\n*\n*");
		
		System.out.println("*");
		System.out.println("*");
		System.out.println("*");
	}
}
